function generatePatientNumber(index: number): string {
  const letterIndex = Math.floor((index - 1) / 26);
  const letter = String.fromCharCode(65 + letterIndex);
  const number = (index - 1) % 10000;
  return `PA${number.toString().padStart(5, "0")}${letter}`;
}

